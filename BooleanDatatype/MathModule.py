#ceil method return next integer bigger than the given number
print(math.ceil(-2.99))
print(math.ceil(3.12))
#floor method give next integer smaller than the given number
print(math.floor(-2.12))
print(math.floor(3.99))
# fabs returns the absolute value of given number
print(math.fabs(-3.12))
#round method returns bigger value than given number if floating point is <=50
print(round(3.49))

